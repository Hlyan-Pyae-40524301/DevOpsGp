package com.group1.cw;

/**
 * Represents a City
 */
public class City
{
    /**
     * City's ID
     */
    public int ID;

    /**
     * City's Name
     */
    public String Name;

    /**
     * City's Country Code
     */
    public String CountryCode;

    /**
     * City's District
     */
    public String District;

    /**
     * City's Population
     */
    public int Population;

    /**
     * Total Population
     */
    public long TotalPopulation;
}